    public DashBoard() {
        initComponents();
        try{
            Connection conn=(Connection)DriverManager.getConnection("jdbc:mysql://localhost:3306/db","root","root");
            PreparedStatement st=(PreparedStatement)conn.prepareStatement("SELECT * FROM movie where email=?");
            st.setString(1,Login.userEmail);
            ResultSet rs = st.executeQuery();
            
            if(rs.next()){
                user_name = rs.getString("name");
                user_email = rs.getString("email");
                user_mob = rs.getString("mob");
                user_pass = rs.getString("password");
                user_bal = rs.getInt("balance");
            }
            welcome.setText("Welcome, "+user_name+"!");
        }
        catch(Exception ex){
            System.out.println(ex);
        }
    }
